                                     the measures to prevent devastating events, such as
                                      deforestation, from happening in the first place. Otherwise, it 
                                      will be a race with the clock (just like the game). However, 
                                      I don't mean that we have to suddenly go out there and ban all 
                                      lumberjacks. Taking preventative measures to control the amount of 
                                      paper and products made from trees in our daily lives will make a 
                                      positive impact!
                                </p>
